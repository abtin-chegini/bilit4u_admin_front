import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export const convertDateToNumber = (date: string): string => {
  const persianDigits = "۰۱۲۳۴۵۶۷۸۹";
  const latinDigits = "0123456789";

  // Convert Persian digits to Latin digits
  const convertedDate = date.replace(
    /[۰-۹]/g,
    (digit) => latinDigits[persianDigits.indexOf(digit)]
  );

  // Split the date into parts
  const [year, month, day] = convertedDate.split("/");

  // Pad month and day with leading zeros if necessary
  const paddedMonth = month.padStart(2, "0");
  const paddedDay = day.padStart(2, "0");

  // Combine the parts into a single string
  return `${year}${paddedMonth}${paddedDay}`;
};

export const generateHash = (length: number) => {
  const characters =
    "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
  let result = "";
  const charactersLength = characters.length;
  for (let i = 0; i < length; i++) {
    result += characters.charAt(Math.floor(Math.random() * charactersLength));
  }
  return result;
};

